package proyecto;

public class ConsoleColors {

    // Colores de texto
    public static final String RESET = "\033[0m";  // Texto blanco
    public static final String BLACK = "\033[0;30m";   // Texto negro
    public static final String RED = "\033[0;31m";     // Texto rojo
    public static final String GREEN = "\033[0;32m";   // Texto verde
    public static final String YELLOW = "\033[0;33m";  // Texto amarillo
    public static final String BLUE = "\033[0;34m";    // Texto azul
    public static final String PURPLE = "\033[0;35m";  // Texto morado
    public static final String CYAN = "\033[0;36m";    // Texto cian
    public static final String WHITE = "\033[0;37m";   // Texto blanco

    // Colores de fondo
    public static final String BG_BLACK = "\033[0;40m";   // Fondo negro
    public static final String BG_RED = "\033[0;41m";     // Fondo rojo
    public static final String BG_GREEN = "\033[0;42m";   // Fondo verde
    public static final String BG_YELLOW = "\033[0;43m";  // Fondo amarillo
    public static final String BG_BLUE = "\033[0;44m";    // Fondo azul
    public static final String BG_PURPLE = "\033[0;45m";  // Fondo morado
    public static final String BG_CYAN = "\033[0;46m";    // Fondo cian
    public static final String BG_WHITE = "\033[0;47m";   // Fondo blanco

    // Estilos de texto
    public static final String BOLD = "\033[1m";      // Texto en negrita
    public static final String UNDERLINE = "\033[4m"; // Texto subrayado
    public static final String REVERSED = "\033[7m";  // Texto invertido
}
